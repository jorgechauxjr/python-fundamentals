def sumar(a: int, b: int):
    """
    Suma dos numeros

    Args:
        a (int): primer numero
        b (int): segundo numero

    Returns:
        la suma de a + b
    """
    return a + b

def restar(a: int, b: int):
    """
    Resta dos numeros

    Args:
        a (int): primer numero
        b (int): segundo numero

    Returns:
        la resta de a - b
    """
    return a - b

def multiplicar(a: int, b: int):
    """
    Multiplica dos numeros

    Args:
        a (int): primer numero
        b (int): segundo numero

    Returns:
        la multiplicación de a * b
    """
    return a * b

def dividir(a: int, b: int):
    """
    Divide dos numeros

    Args:
        a (int): primer numero
        b (int): segundo numero

    Returns:
        la división de a / b
    """
    return a / b