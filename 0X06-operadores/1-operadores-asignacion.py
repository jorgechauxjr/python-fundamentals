# Operadores Asignación

x = 5
x += 3 # Suma
print(x)

x -= 3 # Resta
print(x)

x *= 3 # Multiplicación
print(x)

x /= 3 # División. con doble // da entero
print(x)


x %= 2 # Módulo(residuo)
print(x)
print()

y = 20

y //= 2 # División Entera
print(y)

y **= 3 # Exponente 10 * 10 * 10
print(y)
print()

# WALRUS (morsa) :=
print(z := 3)
print(z)